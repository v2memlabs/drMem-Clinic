/// Diğer modüller için geriye dönük uyumluluk (repository ile aynı store).
void addMockPatient(Patient p) => mockPatients.add(p);

String nextMockFileNumber() {
  var maxSeq = 0;
  for (final p in mockPatients) {
    final parts = p.fileNumber.split('-');
    if (parts.length < 3) continue;
    final seq = int.tryParse(parts.last);
    if (seq != null && seq > maxSeq) maxSeq = seq;
  }
  final next = maxSeq + 1;
  return 'H-2026-${next.toString().padLeft(4, '0')}';
}

String mockPatientName(String patientId) {
  for (final p in mockPatients) {
    if (p.id == patientId) return p.fullName;
  }
  return 'Bilinmeyen Hasta';
}
